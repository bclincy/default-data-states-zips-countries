function saveFile (array $data, string $fileName, string $type = 'YAML'): bool 
{
    return $type === 'JSON' ? file_put_contents($fileName .'.json', json_encode($data)) 
        : file_put_contents($fileName . '.yaml', Yaml::dump($data));
}


